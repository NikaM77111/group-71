# Addition (+)
print("Addition:")
print("3 + 7 =", 3 + 7)
print("10 + 15 =", 10 + 15)
print("0 + 100 =", 0 + 100)
print("-5 + 8 =", -5 + 8)
print("20 + (-3) =", 20 + (-3))

# Subtraction (-)
print("\nSubtraction:")
print("10 - 4 =", 10 - 4)
print("50 - 100 =", 50 - 100)
print("-10 - (-5) =", -10 - (-5))
print("0 - 0 =", 0 - 0)
print("7 - 2 =", 7 - 2)

# Multiplication (*)
print("\nMultiplication:")
print("4 * 5 =", 4 * 5)
print("-3 * 6 =", -3 * 6)
print("0 * 100 =", 0 * 100)
print("7 * (-2) =", 7 * (-2))
print("-4 * (-4) =", -4 * (-4))

# Division (/)
print("\nDivision:")
print("10 / 2 =", 10 / 2)
print("9 / 3 =", 9 / 3)
print("7 / 2 =", 7 / 2)
print("-15 / 5 =", -15 / 5)
