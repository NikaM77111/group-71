#2.
#== (ტოლობა)
print(5 == 5)       # True
print("abc" == "ab")  # False
#!= (არაჰეთობა)
print(3 != 4)        # True
print("hi" != "hi")  # False
#> (მეტობა)
print(10 > 3)        # True
print(2 > 5)         # False
#< (ნაკლებობა)
print(7 < 10)        # True
print(10 < 3)        # False
#>= (მეტია ან ტოლი)
print(5 >= 5)        # True
print(4 >= 6)        # False
#<= (ნაკლებია ან ტოლი)
print(2 <= 2)        # True
print(7 <= 1)        # False
