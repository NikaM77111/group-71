# Sequence (მიმდევრობა)
print("1. Wake up")
print("2. Brush teeth")
print("3. Eat breakfast")

# Selection (არჩევა)
is_raining = True
if is_raining:
    print("Take an umbrella")
else:
    print("No need for umbrella")

# Iteration (ციკლი)
for i in range(1, 6):
    print(f"Step {i}: Walking to school")
