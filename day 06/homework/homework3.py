# ვქმნით ორ ცვლადს და ვანიჭებთ რიცხვებით მნიშვნელობას
a = 10
b = 5

# ვახორციელებთ შეკრებას
sum_result = a + b  # 10 + 5 = 15

# ვახორციელებთ გამოკლებას
difference = a - b  # 10 - 5 = 5

# ვახორციელებთ გამრავლებას
product = a * b  # 10 * 5 = 50

# ვახორციელებთ გაყოფას
division = a / b  # 10 / 5 = 2.0

# ვპოულობთ დანარჩენს გაყოფისას (მოდულო ოპერაცია)
modulus = a % b  # 10 % 5 = 0


# ვახორციელებთ მთელი რიცხვით გაყოფას
floor_division = a // b  # 10 // 5 = 2

# ვბეჭდავთ ყველა შედეგს
print("შეკრება:", sum_result)
print("გამოკლება:", difference)
print("გამრავლება:", product)
print("გაყოფა:", division)
print("დანარჩენი:", modulus)
print("მთელი გაყოფა:", floor_division)
