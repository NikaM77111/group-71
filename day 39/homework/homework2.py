# .lower() - სტრინგის ყველა ასოს გადაჰყავს პატარა ასოებში
print("HELLO".lower())  # გამოსავალი: hello

# .upper() - სტრინგის ყველა ასოს გადაჰყავს დიდი ასოებში
print("hello".upper())  # გამოსავალი: HELLO

# .capitalize() - მხოლოდ პირველი ასოს აქცევს დიდად, დანარჩენს კი პატარად
print("hELLo".capitalize())  # გამოსავალი: Hello

# .find() - აბრუნებს სტრინგში სიმბოლოს ან ქვეწერის პირველივე გამოჩენის ინდექსს
print("hello".find("e"))  # გამოსავალი: 1

# .count() - ითვლის კონკრეტული სიმბოლოს ან ქვეწერის რაოდენობას სტრინგში
print("banana".count("a"))  # გამოსავალი: 3
