    print(i)  # рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃљрЃЋрЃА 0-рЃЊрЃљрЃю 4-рЃЏрЃЊрЃћ рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃА

