x = 10
y = 5

# ვამატებთ x და y-ს
sum_result = x + y  # 10 + 5 = 15

# ვაკლებთ y-ს x-ს
diff_result = x - y  # 10 - 5 = 5

# ვამრავლებთ x-ს y-ზე
mul_result = x * y  # 10 * 5 = 50

# x-ს ვყოფთ y-ზე
div_result = x / y  # 10 / 5 = 2.0

# ვბეჭდავთ შედეგებს
print("Sum:", sum_result)        # გამოიტანს: Sum: 15
print("Difference:", diff_result)  # გამოიტანს: Difference: 5
print("Multiplication:", mul_result)  # გამოიტანს: Multiplication: 50
print("Division:", div_result)    # გამოიტანს: Division: 2.0
