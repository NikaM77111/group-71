fahrenheit = float(input("შეიყვანეთ ტემპერატურა ფარენგეიტში: "))
celsius = (fahrenheit - 32) * 5/9
print("ტემპერატურა ცელსიუსშია:", celsius)

## მომხმარებელს შეჰყავს ტემპერატურა ფარენგეიტში
f = float(input("შეიყვანე ტემპერატურა ფარენგეიტში: "))

# ფორმულა: C = (F - 32) * 5/9
c = (f - 32) * 5/9

print("ტემპერატურა ცელსიუსშია:", c)
