# ­ЪЪА 7. Input - рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃАрЃњрЃљрЃю рЃўрЃюрЃцрЃЮрЃарЃЏрЃљрЃфрЃўрЃўрЃА рЃЏрЃўрЃдрЃћрЃЉрЃљ

