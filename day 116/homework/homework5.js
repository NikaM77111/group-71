/*splice-рЃўрЃА рЃАрЃўрЃюрЃбрЃљрЃЦрЃАрЃў + рЃЊрЃљрЃЋрЃљрЃџрЃћрЃЉрЃљ
­Ъћ╣ splice-рЃўрЃА рЃАрЃўрЃюрЃбрЃљрЃЦрЃАрЃў:
array.splice(startIndex, deleteCount, item1, item2, ...)

startIndex РђЊ рЃарЃЮрЃЏрЃћрЃџрЃў рЃўрЃюрЃЊрЃћрЃЦрЃАрЃўрЃЊрЃљрЃю рЃЊрЃљрЃўрЃгрЃДрЃЮрЃА

deleteCount РђЊ рЃарЃљрЃЏрЃЊрЃћрЃюрЃў рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃў рЃгрЃљрЃерЃљрЃџрЃЮрЃА

item РђЊ (рЃљрЃарЃљрЃАрЃљрЃЋрЃљрЃџрЃЊрЃћрЃЉрЃБрЃџрЃЮ) рЃЊрЃљрЃЏрЃљрЃбрЃћрЃЉрЃБрЃџрЃў рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃћрЃЉрЃў

­Ъћ╣ рЃЏрЃЮрЃфрЃћрЃЏрЃБрЃџрЃў рЃЏрЃљрЃАрЃўрЃЋрЃў:*/
let arr = [10, 20, 30, 40, 50];

// 30 рЃЊрЃљ 40-рЃўрЃА рЃћрЃарЃЌрЃЊрЃарЃЮрЃБрЃџрЃў рЃгрЃљрЃерЃџрЃљ
arr.splice(2, 2);

console.log(arr); // [10, 20, 50]